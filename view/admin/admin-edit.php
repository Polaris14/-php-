<script>layui.use(['form', 'layer'],
        function() {
            $ = layui.jquery;
            var form = layui.form,
                layer = layui.layer;

            //自定义验证规则
            form.verify({
                nikename: function(value) {
                    if (value.length < 5) {
                        return '昵称至少得5个字符啊';
                    }
                },
                pass: [/(.+){6,12}$/, '密码必须6到12位'],
                repass: function(value) {
                    if ($('#L_pass').val() != $('#L_repass').val()) {
                        return '两次密码不一致';
                    }
                }
            });

            //监听提交
            form.on('submit(add)',
                function(data) {
                    console.log(data);
                    //发异步，把数据提交给php
                    var switchI = data.field.switch;
                    if(switchI == 'on'){
                        data.field.is_super = '1';
                    }else{
                        data.field.is_super = '0';
                    }
                    data.field.id = <?php echo $id?>;
                    $.ajax({
                        url:'../../controller/user.php?tag=updateById',
                        type:'post',
                        dataType:'json',
                        data:data.field,
                        success:function(res){
                            if(res.code){
                                layer.alert("修改成功", {
                                        icon: 6
                                    },
                                    function() {
                                        //关闭当前frame
                                        xadmin.close();

                                        // 可以对父窗口进行刷新
                                        xadmin.father_reload();
                                    });
                            }else{
                                alert(res.message);
                            }
                        }
                    });
                    return false;
                });

        });
</script>
